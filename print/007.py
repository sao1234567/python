## 四則演算
# 足し算
print(3 + 3)

# 引き算
print(3 - 3)

# 掛け算
print(3 * 3)

# 割り算
print(3 / 3)

# 割り算(切り捨て)
print(4 // 3)

# 累乗
print(3 ** 3)

# 余り
print(4 % 3)

# ()
print((3 + 3) * 3)


## 基本的に使うのはここまで


## >>
# 2進数に直して右に移動
# 4 -> 100 -> 10 -> 2
print(4 >> 1)

# 11 -> 1011 -> 10 -> 2
print(11 >> 2)

## <<
# 2進数に直して左に移動
# 4 -> 100 -> 1000 -> 8
print(4 << 1)

# 20 -> 10100 -> 1010000 -> 80
print(20 << 2)